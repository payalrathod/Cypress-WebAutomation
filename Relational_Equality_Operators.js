//Relational or comparison operators

// > -more than
// < -less than
// >= -more than equal
// <= -less than equal

var result = 10 > 5
console.log(result)  //true
var result = 10 < 5
console.log(result)  //false
var result = 5 > 5
console.log(result)  //false
var result = 5 >= 5
console.log(result)  //true
var result = 6 >= 5
console.log(result)  //true

